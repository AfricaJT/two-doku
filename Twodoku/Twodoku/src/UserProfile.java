
public class UserProfile {
	public static int width;
	public static int mode;
	
	public UserProfile () {
		
	}
	
	public static void setWidth (int w){
		width = w;
	}
	public static void setMode (int m){
		mode = m;
	}
	public static int getWidth () {
		return width;
	}
	public static int getMode () {
		return mode;
	}
}
